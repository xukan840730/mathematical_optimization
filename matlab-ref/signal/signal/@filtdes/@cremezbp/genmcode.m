%   $Revision: 1.1.8.3 $  $Date: 2004/12/26 22:12:22 $

[params, values, descs, iargs] = cremez_genmcode(d);
[fs fsstr]                     = getfsstr(d);

b = sigcodegen.mcodebuffer;

b.addcr(b.formatparams({'N', 'Fstop1', 'Fpass1', 'Fpass2', 'Fstop2', 'Fstop3', 'Fpass3', ...
        'Fpass4', 'Fstop4', 'Wstop1', 'Wpass1', 'Wstop2', 'Wpass2', 'Wstop3', params{:}}, ...
    {getmcode(d, 'Order'), getmcode(d, 'Fstop1'), getmcode(d, 'Fpass1'), ...
        getmcode(d, 'Fpass2'), getmcode(d, 'Fstop2'), getmcode(d, 'Fstop3'), ...
        getmcode(d, 'Fpass3'), getmcode(d, 'Fpass4'), getmcode(d, 'Fstop4'), ...
        getmcode(d, 'Wstop1'), getmcode(d, 'Wpass1'), getmcode(d, 'Wstop2'), ...
        getmcode(d, 'Wpass2'), getmcode(d, 'Wstop3'), values{:}}, ...
    {'','','','','','','','','','','','','','',descs{:}}));
b.cr;
b.addcr(designdesc(d));
b.addcr('b  = cfirpm(N, %s, ''bandpass'', %s%s);', ...
    sprintf('[-%s Fstop1 Fpass1 Fpass2 Fstop2 Fstop3 Fpass3 Fpass4 Fstop4 %s]%s', fsstr, fsstr, fs), ...
    '[Wstop1 Wpass1 Wstop2 Wpass2 Wstop3]', iargs);
b.add('Hd = dfilt.dffir(b);');

